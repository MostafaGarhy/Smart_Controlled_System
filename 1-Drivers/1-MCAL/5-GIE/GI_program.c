#include "STD_TYPES.h"
#include "BIT_MATH.h"
#include "GI_interface.h"

void GI_VidEnable(void)
{
	SET_BIT(GI_u8_SREG,SREG_I_BIT);
}

void GI_VidDisable(void)
{
	CLR_BIT(GI_u8_SREG,SREG_I_BIT);
}